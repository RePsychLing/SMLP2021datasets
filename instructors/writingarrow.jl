md"""
# Creating Arrow files in Julia

Data frames stored in [Arrow](https://arrow.apache.org) can be read using many different languages.
Julia provides very good facilities for creating Arrow files.

This notebook illustrates creating Arrow files from a CSV source and from RData or RDS files.

The conversion itself is straightforward.
It can be worthwhile doing some data cleaning and transformation during these conversion steps.

## Simple conversion

Consider the conversion of the CSV file `full_axb.csv` provided by Patricia Amillos.

First, load the packages to be used.
"""

# ╔═╡ de931048-9d8a-4752-a170-2b6179470dec
md"""
Read the CSV file and convert to a DataFrame.
"""

# ╔═╡ 96d01ab2-7b43-41c8-9780-bbd3bd6053e5
axb_data = CSV.read("../Patricia_Amillos/full_axb.csv", DataFrame)

# ╔═╡ df69d7d5-ea2d-4b37-804b-7c4eb5e3c91c
describe(axb_data)

# ╔═╡ 4432e088-5daa-46c5-b344-e5b28fe8df52
typeof.(eachcol(axb_data))

# ╔═╡ 35ba4c00-9962-46c2-9ad4-8d78e3b2c3b2
md"""
This DataFrame can be written to an Arrow file without modification.
"""

# ╔═╡ b3cfca7a-dd18-4fa9-b010-705458ccafa6
axb_full_arrow = Arrow.write("./data/full_axb.arrow", axb_data; compress=:zstd);

# ╔═╡ 867462e3-11ea-41b8-bc7a-4aa2fc4afe5b
md"""
[Zstandard](https://en.wikipedia.org/wiki/Zstandard) compression is quite effective in this case in reducing the file size.
The Arrow file is about 1/10 the size of the CSV file.
"""

# ╔═╡ 4e954cf5-ba20-497b-8bbc-3d84d5fb624d
filesize(axb_full_arrow)

# ╔═╡ 2983ed07-8790-412d-8ab7-52d1edf913cb
filesize("../Patricia_Amillos/full_axb.csv")

# ╔═╡ beebe630-2c4e-4c4b-9fbf-e97044dc4916
md"""
## Restoring the data frame from the arrow file

The function to read an Arrow file is `Arrow.Table` which produces a column table.
Often this table is converted to a `DataFrame` for further manipulation.
"""

# ╔═╡ b7269fc0-6e8e-4a6d-955e-a35ba2ab3406
axb_from_arrow = Arrow.Table(axb_full_arrow)

# ╔═╡ 3e9cda79-3843-4a8c-af4e-9334d63a982e
md"""
Also, behind the scenes, the `Arrow.write` function has converted some of the categorical covariates to more compact storage formats.
In Arrow terminology the storage type for a factor or categorical variable is called `DictEncoded`.
"""

# ╔═╡ 8429788b-f1a5-4e30-a913-80831c16ebe7
typeof.(axb_from_arrow)

