# ╔═╡ 81d362bf-589c-4aa6-abab-2097b9a5f60c
md"""
## Basic writing and reading of arrow files

Writing and reading an arrow file in Julia is straightforward.
Typically the file is written as a compressed file and the Zstandard compression is quite effective on such files.
An uncompressed file gives maximal performance when reading an arrow file, because it can be [memory-mapped](https://en.wikipedia.org/wiki/Memory-mapped_file).
However, the uncompressed file requires more storage and more information to be transmitted when download the file, for example.

The structure of these files can be checked in R and in Python.
"""

# ╔═╡ 16418fe6-7fc2-47d2-92fb-2e4949c552fd
feather = pyimport("pyarrow.feather"); 

# ╔═╡ 62df14a1-52c5-4bcc-93ab-a8646dc67d7c
feather.read_table(som_arrow)

# ╔═╡ 44c58069-8c0f-469c-ad29-263b1174d5ca
md"""
This shows that the `orthaccu` column is being stored as a column of 64-bit integers when, in fact, it is a boolean value.
It would be better to convert it to `Bool` before storing it.

For R, first load the `arrow` and `tibble` packages then read the file.
For historical reasons this format is called "feather" in R.
"""

# ╔═╡ 4098bead-379d-42a4-b906-cc44967e9796
R"""
library("arrow")
library("tibble")
som <- read_feather($som_arrow)  # interpolate the file name from Julia
glimpse(som)
"""

